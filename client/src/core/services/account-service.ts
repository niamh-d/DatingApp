import { HttpClient } from '@angular/common/http';
import { inject, Injectable, signal } from '@angular/core';
import type { LoginCreds, RegisterCreds, User } from '../../types/user';
import { tap } from 'rxjs';
import { environment } from '../../environments/environment';
import { LikesService } from './likes-service';
import { PresenceService } from './presence-service';
import { HubConnectionState } from '@microsoft/signalr';

@Injectable({
  providedIn: 'root',
})
export class AccountService {
  private http = inject(HttpClient);
  private likesService = inject(LikesService);
  public currentUser = signal<User | null>(null);
  private baseUrl = environment.apiUrl;
  private presenceService = inject(PresenceService);

  register(creds: RegisterCreds) {
    return this.http
      .post<User>(`${this.baseUrl}account/register`, creds, { withCredentials: true })
      .pipe(
        tap((user) => {
          if (user) {
            this.setCurrentUser(user);
            this.startTokenRefreshInterval();
          }
        })
      );
  }

  login(creds: LoginCreds) {
    return this.http
      .post<User>(`${this.baseUrl}account/login`, creds, { withCredentials: true })
      .pipe(
        tap((user) => {
          if (user) {
            this.setCurrentUser(user);
            this.startTokenRefreshInterval();
          }
        })
      );
  }

  refreshToken() {
    return this.http.post<User>(
      `${this.baseUrl}account/refresh-token`,
      {},
      {
        withCredentials: true,
      }
    );
  }

  startTokenRefreshInterval() {
    const INTERVAL = 7 * 24 * 60 * 60 * 1000; // for demo app, only call to API for new refresh token every 7 days

    setInterval(() => {
      this.http
        .post<User>(
          `${this.baseUrl}account/refresh-token`,
          {},
          {
            withCredentials: true,
          }
        )
        .subscribe({
          next: (user) => {
            this.setCurrentUser(user);
          },
          error: () => {
            this.logout();
          },
        });
    }, INTERVAL);
  }

  setCurrentUser(user: User) {
    user.roles = this.getRolesFromToken(user);
    this.currentUser.set(user);
    this.likesService.getLikeIds();
    if (this.presenceService.hubConnection?.state !== HubConnectionState.Connected) {
      this.presenceService.createHubConnection(user);
    }
  }

  logout() {
    this.http.post(`${this.baseUrl}account/logout`, {}, { withCredentials: true }).subscribe({
      next: () => {
        localStorage.removeItem('filters');
        this.likesService.clearLikeIds();
        this.currentUser.set(null);
        this.presenceService.stopHubConnection();
      },
    });
  }

  private getRolesFromToken(user: User): string[] {
    const payload = user.token.split('.')[1];
    const decoded = atob(payload);
    const jsonPayload = JSON.parse(decoded);
    return Array.isArray(jsonPayload.role) ? jsonPayload.role : [jsonPayload.role];
  }
}
